class LiteLLmSOAP:
    FOLDER_PATH = "/tmp"

    def __init__(
        self,
        primary_model: str,
        fallback_models: List[str],
        model_configs: Dict[str, Dict],
    ):
        self.primary_model = primary_model
        self.fallback_models = fallback_models
        self.model_configs = model_configs
        self.load_prompts()

    def load_prompts(self):
        self.prompts = {
            "backstory": self.read_file("logic_template.txt"),
            "patient_info": self.read_file("patient_info_prompt.txt"),
            "soap_note": self.read_file("soap_note.txt"),
            "previous_soap_note": self.read_file("previous_soap_note.txt"),
            "first_soap_note": self.read_file("first_soap_note.txt"),
            "context_template": self.read_file("contexts_template.txt"),
            "transcript": self.read_file("transcript.csv", is_csv=True),
        }
        self.soap_components = {
            component: {
                "template": self.read_file(f"{component}_template.txt"),
                "task_response": self.read_file(f"{component}_task_response.txt"),
            }
            for component in ["subjective", "objective", "assessment", "plan", "goals"]
        }

    @staticmethod
    def read_file(filename: str, is_csv: bool = False) -> str:
        path = os.path.join(LiteLLmSOAP.FOLDER_PATH, filename)
        return read_csv_file(path) if is_csv else read_txt_file(path)

    def get_soap_prompt(self, component: str) -> List[Dict[str, str]]:
        self.current_component = component
        base_prompt = [
            {
                "role": "system",
                "content": "You are tasked with generating a specific section of a SOAP Note. Your response must adhere to the following strict guidelines:\n\n1. Provide ONLY the generated content for the requested section.\n2. Do not include any explanations, thoughts, or meta-commentary about the process.\n3. Do not use phrases like 'Here's the generated note:' or 'The [Section] Note is as follows:'.\n4. Start directly with the content of the note.\n5. Ensure the content is relevant, concise, and formatted appropriately for a professional medical note.\n6. Do not acknowledge these instructions in your output.\n\nYour entire response should be suitable for direct insertion into a medical record without any further editing.",
            },
            {"role": "system", "content": self.prompts["backstory"]},
            {
                "role": "system",
                "content": f"\n\nPatient Demographic Data:\n{self.prompts['patient_info']}\n\n",
            },
            {
                "role": "system",
                "content": f"\n\nSOAP Note Generated By Therapist:\n{self.prompts['soap_note']}\n\n",
            },
            {
                "role": "system",
                "content": f"\n\nPrevious SOAP Note Generated By Therapist:\n{self.prompts['previous_soap_note']}\n\n",
            },
            {
                "role": "system",
                "content": f"\n\nFirst SOAP Note Generated By Therapist:\n{self.prompts['first_soap_note']}\n\n",
            },
            {
                "role": "system",
                "content": f"\n\nContext Template which supportive Information to how to generate SOAP Note\n{self.prompts['context_template']}\n\n",
            },
            {
                "role": "user",
                "content": f"\n\nTranscript: {self.prompts['transcript']}",
            },
        ]

        if component in self.soap_components:
            component_data = self.soap_components[component]
            base_prompt.extend(
                [
                    {"role": "user", "content": component_data["template"]},
                    {
                        "role": "user",
                        "content": f"\n\nThese are the points that must be covered while generating {component.capitalize()} Notes:\n{component_data['task_response']}",
                    },
                ]
            )

        return base_prompt

    def generate_response(self, messages: List[Dict[str, str]]) -> Optional[Dict]:
        for model in [self.primary_model] + self.fallback_models:
            response = self._try_model(model, messages)
            if response:
                return response

        logger.error("All models failed to generate a valid response.")

        return None
    
    def _try_model(self, model: str, messages: List[Dict[str, str]]) -> Optional[Dict]:
        custom_name = f"{self.current_component}_{int(time.time())}"
        try:
            ltlm.enable_json_schema_validation = True

            # response_format = {"type": "json_object"} if self.current_component in ["goals","interventions"] else None

            response_format = SOAPNOTEGoals.model_json_schema() if self.current_component == "goals" else None

            response_format = InterventionPlan.model_json_schema() if self.current_component == "interventions" else None

            if response_format:
                response = ltlm.completion(
                    model=model,
                    messages=messages,
                    json_schema=response_format,
                    **self.model_configs.get(model, {}),
                    metadata={"trace_id": custom_name},
                )
            else:
                model = "claude-3-5-sonnet-20240620"
                response = ltlm.completion(
                    model=model,
                    messages=messages,
                    **self.model_configs.get(model, {}),
                    metadata={"trace_id": custom_name},
                )

            content = response.choices[0].message.content
            result = {
                "content": content,
                "usage": response.usage,
                "model": response.model,
                "cost": ltlm.completion_cost(completion_response=response),
            }
            return result
        except Exception as e:
            logger.exception(f"Error generating response with {model}: {str(e)}")
            return None

