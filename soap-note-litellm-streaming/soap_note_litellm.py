def litellm_main(room_id: str, template_id: str, session_id: str, session_duration: int):
    primary_model = "together_ai/meta-llama/Llama-3.3-70B-Instruct-Turbo"
    fallback_models = ["claude-3-5-sonnet-20240620"]

    # primary_model = "claude-3-5-sonnet-20240620"
    # fallback_models = ["together_ai/meta-llama/Llama-3.3-70B-Instruct-Turbo"]
    
    model_configs = {
        "claude-3-5-sonnet-20240620": {"max_tokens": 5000, "temperature": 0.1},
        "together_ai/meta-llama/Llama-3.3-70B-Instruct-Turbo": {
            "max_tokens": 5000,
            "temperature": 0.1,
        },
    }

    all_note_sections = [
        "subjective",
        "objective",
        "assessment",
        "plan",
        "interventions",
        "goals",
    ]
    soap = LiteLLmSOAP(primary_model, fallback_models, model_configs)
    final_result = {
        "booking_id": room_id,
        "template_id": template_id,
        "session_id": session_id,
        "status": "in_progress",
    }
    for index, section in enumerate(all_note_sections, start=1):
        final_result = soap.process_soap_section(section, final_result, session_duration)
        if index == len(all_note_sections):
            final_result["status"] = "completed"
    
    logger.info(f"Calling Webhook to insert Generated SOAP Note")
    webhook_for_inserting_into_theranow(data=final_result)
    save_final_result(final_result)
    webhook_for_inserting_generated_notes_with_booking_id(data=final_result)
    return final_result

