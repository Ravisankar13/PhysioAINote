    def process_soap_section(self, section: str, final_result: Dict, session_duration: int = None) -> Dict:
        messages = self.get_soap_prompt(component=section)
        
        if section == "goals":
            stg_example = load_json("stg_examples.json")
            ltg_example = load_json("lgt_examples.json")
            goal_category_examples = read_txt_file("goal_category.txt")

            messages.append({
                "role": "system",
                "content": f"Here are examples of short-term goals (STG) and long-term goals (LTG) to guide your response:\n\nSTG Example:\n{json.dumps(stg_example, indent=2)}\n\nLTG Example:\n{json.dumps(ltg_example, indent=2)}"
            })

            messages.append({
                "role": "system",
                "content": f"""Here are correct set of goal_category values. So while generating the output make sure the goal_category should be from this text only\n Goal Category:\n{goal_category_examples}"""
            })
            
        elif section == "interventions" and session_duration is not None:
            messages.append({
                "role": "system",
                "content": "You are a medical professional tasked with generating interventions for a therapy session. Your response must be in JSON format, adhering to strict guidelines."
            })
            messages.append({
                "role": "user",
                "content": f"""
    Generate interventions for a therapy session lasting {session_duration} minutes. Adhere to the following guidelines:
    1. Each intervention must be exactly 8 minutes long.
    2. The total number of interventions should equal the session duration divided by 8, rounded to the nearest whole number.
    3. Use appropriate CPT codes for each intervention.
    4. For sessions longer than 16 minutes, use a variety of intervention types (e.g., Therapeutic Exercise, Neuromuscular Reeducation, Manual Therapy).
    5. For interventions that continue beyond 8 minutes, label them as "(continued)" in subsequent entries.
    Respond with a JSON object containing an "interventions" array and a "total_time" field, where each intervention follows this format:
    {{
    "intervention_number": "Intervention number (e.g., 1, 2, 3)",
    "cpt_code": "CPT code for the intervention",
    "description": "Name of the intervention(3-4 line explaining in detail)",
    "duration": "8 minutes",
    "units": 1,
    "justification": " a brief Brief medical justification(3-4 line explaining in detail)"
    }}

    This is only for Manual Therapy CPT Code: you should only say its a manual if
    -> if only if therapist moves the body parts by touching patient then only consider manual therapy

    Do not include any additional text or explanations outside of the JSON object.

    These are the list of CPT Code which should be mentioned in the output because can't be use as per the Insurance Company, Hospital and Therapist:
    [97140]
    """
            })

        response = self.generate_response(messages)
        if response:
            logger.info(f"{section.capitalize()} response generated successfully")
            logger.debug(f"Model used: {response['model']}")
            logger.debug(f"Total tokens used: {response['usage']['total_tokens']}")
            logger.debug(f"Estimated cost: ${response['cost']:.6f}")
            
            section_content = response["content"]
            
            if section in ["goals", "interventions"]:
                # Remove triple backticks, newlines, and the "json" keyword
                section_content = section_content.strip('`\n').lstrip('json\n')
                try:
                    section_content = json.loads(section_content)
                except json.JSONDecodeError as e:
                    logger.error(f"Error parsing JSON for {section}: {str(e)}")
                    logger.error(f"Raw content: {section_content}")
                    section_content = response["content"]
            else:
                section_content = re.sub(r"^.*?:\s*", "", section_content, flags=re.IGNORECASE)
            

            if "additionalProperties" in section_content:
                section_content = section_content.get("additionalProperties",{})
                # if "interventions" in section_content:
                #     section_content = section_content.get("interventions",[])
            
            if "json_payload" in section_content:
                section_content  = section_content.get("json_payload")
                
            final_result[section] = {"full_note": section_content}
        else:
            logger.error(f"Failed to generate {section} response")

        return final_result

